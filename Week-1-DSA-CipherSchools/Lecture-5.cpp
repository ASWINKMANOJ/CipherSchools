#include <iostream>
using namespace std;

int main()
{
    int a, b, c, d, e;

    cout << "Enter Five numbers: " << endl;

    cin >> a >> b >> c >> d >> e;

    cout << "The numbers in reverse in order : " << endl;
    cout << e << " " << d << " " << c << " " << b << " " << a << endl;

    return 0;
}